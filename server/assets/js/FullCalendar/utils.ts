const notCallback = (item: string): boolean => callbacks.indexOf(item) === -1;
const notMethod = (item: string): boolean => methods.indexOf(item) === -1;

const isCallback = (callbackName: string): boolean => !notCallback(callbackName);
const isMethod = (methodName: string): boolean => !notMethod(methodName);
const isOption = (optionName: string): boolean => notCallback(optionName) && notMethod(optionName);

